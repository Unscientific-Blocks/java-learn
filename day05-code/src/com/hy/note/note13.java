package com.hy.note;

public class note13 {
    public static void main(String[] args) {
//        数组动态初始化
//        格式：
//        数据类型 数组名[] = new 数据类型[数组的长度]
        int name[] = new int[50];
        for (int i = 0; i < name.length; i++) {
            System.out.println(name[i]);
        }
    }
}
