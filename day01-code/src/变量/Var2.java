package 变量;

public class Var2 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);


//        int a = 10;
        int b = 20;
        System.out.println(a + b);

        a = 30;
        System.out.println(a);
        System.out.println(a + b);
//在一条语句中，可以定义多个变量
        int c = 100, d = 200, e = 300;
        System.out.println("c" + "d" + "e");

        int f;

        f = 100;
        System.out.println(f);
    }
}
